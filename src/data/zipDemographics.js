// Get demographic info for a ZIP code
export function getZipDemographic(zipCode) {
  const zip3 = zipCode.toString().padStart(3, '0');
  return demographicProfiles[zip3] || {
    segment: 'Mixed Demographics',
    income: 'Middle',
    autoOwnership: 'Medium'
  };
}

// Estimate reach based on ZIP code characteristics
export function estimateZipReach(zipCode) {
  const demographic = getZipDemographic(zipCode);
  
  // Base reach varies by auto ownership
  const baseReach = {
    'High': 45000,
    'Medium': 32000,
    'Low': 18000
  }[demographic.autoOwnership] || 30000;
  
  // Add some variance
  const variance = Math.floor(Math.random() * 10000) - 5000;
  return baseReach + variance;
}
